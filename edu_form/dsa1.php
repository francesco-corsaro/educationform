      		<h2>Quali sono i disturbi specifici dell’apprendimento e come riconoscerli</h2>
      		<p><strong>I disturbi specifici dell’apprendimento (DSA)</strong> fanno parte di un gruppo di disabilità  che richiede allo studente un maggiore sforzo nell’apprendimento e utilizzo della lettura, della scrittura e del calcolo. È definito specifico perché il disturbo interessa uno specifico e circoscritto dominio di abilità scolastica conservando integro il funzionamento intellettivo generale.</p>
    		<p><strong>Dislessia</strong> si intende una disabilità specifica dell’apprendimento che si manifesta con una difficoltà nell'imparare a leggere, in particolare nella decifrazione dei segni linguistici, ovvero nella correttezza e nella rapidità della lettura. Lo studente riporta difficoltà nell’effettuare un lettura accurata e fluente. Nei primi anni di scuola mostra incertezze nel riconoscimento delle lettere dell’alfabeto, nella corrispondenza grafema-fonema e quindi ad automatizzare la conversione dei simboli in suoni. Queste criticità si ripercuotono sia all’interno dell’ambiente scolastico che nelle attività di vita quotidiana.</p>
    		<p><strong>Discalculia</strong> si intende una disabiltà specifica che si manifesta con difficoltà negli automatismi del calcolo e dell’elaborazione dei numeri. Lo studente mostra criticità a comprendere ed automatizzare operazioni con i numeri. Lo studente può manifestare incertezze nel cogliere i meccanismi di quantificazione, associazione numero quantità, ed eseguire calcoli a mente (nello specifico recuperare i risultati delle tabelline, recupero dei fatti numerici e algoritmo del calcolo scritto).</p>
    		<p><strong> Disgrafia </strong>si intende una disabilità specifica della scrittua che si manifesta in difficoltà nella realizzazione grafica. Ha a che fare con la fase esecutiva della scrittura che evidenzia una grafia poco leggibile. Lo studente disgrafico a volte presenta una cattiva impugnatura della penna o matita, difficoltà a occupare lo spazio nel foglio con alternanza tra macro e micrografia.</p>
    		<p><strong>Disortografia </strong>si intende per disortografia una disabilità specifica di scrittura che si manifesta in difficoltà nei processi linguistici di transcodifica. Lo studente mostra criticità nella fase costruttiva della scrittura, quindi difficoltà di scrivere in modo corretto da un punto di vista ortografico. Ne consegue un maggiore sforzo nell’applicare le regole di conversione dal suono alla parola scritta e quindi a riconoscere i suoni che compongono la parola e a trovare la giusta sequenza per comporla nel foglio.</p>
    		
